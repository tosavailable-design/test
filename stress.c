#include <stdio.h>
#include <stdint.h>

int main() {
    // Миллиард итераций.
    uint64_t iterations = 1000000000ULL;
    uint64_t a = 0, b = 0, c = 0, d = 0;

    printf("Начинаю насиловать процессор независимыми инструкциями...\n");

    // Цикл. Заставляем проц выполнять 4 независимых сложения за итерацию.
    // __asm__ volatile — это приказ компилятору: "РУКИ ПРОЧЬ, НЕ ОПТИМИЗИРУЙ ЭТО!"
    for (uint64_t i = 0; i < iterations; i++) {
        __asm__ volatile (
            "add $1, %[a]\n\t"
            "add $1, %[b]\n\t"
            "add $1, %[c]\n\t"
            "add $1, %[d]\n\t"
            : [a] "+r" (a), [b] "+r" (b), [c] "+r" (c), [d] "+r" (d)
        );
    }

    // Выводим результат, чтобы компилятор не решил, 
    // что переменные a, b, c, d вообще не нужны.
    printf("Готово. Магическое число: %lu\n", a + b + c + d);
    
    return 0;
}
